package dto;

public class Product {
	private int id;
	private String model;
	private String brand;
	public int getId() {
		return id;
	}
	public String getModel() {
		return model;
	}
	public String getBrand() {
		return brand;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}

}
